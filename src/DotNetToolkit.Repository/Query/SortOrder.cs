namespace DotNetToolkit.Repository.Query
{
    /// <summary>
    /// Represents a sorting order for the <see cref="IQueryOptions{T}" />.
    /// </summary>
    public enum SortOrder
    {
        /// <summary>
        /// Represents an ascending sorting order.
        /// </summary>
        Ascending = 0,
        /// <summary>
        /// Represents a descending sorting order.
        /// </summary>
        Descending = 1
    }
}
